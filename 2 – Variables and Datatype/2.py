# Operators 

# Arithmetic Operators
print("Arithmetic Operators")
a=10
b=2
print("a + b =", a + b) # Addition
print("a - b =", a - b) # Subtraction
print("a * b =", a * b) # Multiplication
print("a / b =", a / b) # Division
print("a % b =", a % b) # Modulus
print("a ** b =", a ** b) # Exponentiation
print("a // b =", a // b) # Floor Division

# Assignment Operators
print("\nAssignment Operators")
a = 10
print("a =", a) # Assigns 10 to a
a += 5 # Adds 5 to a
print("a after += 5:", a) # Now a is 15
a -= 3 # Subtracts 3 from a
print("a after -= 3:", a) # Now a is 12
a *= 2 # Multiplies a by 2
print("a after *= 2:", a) # Now a is 24
a /= 4 # Divides a by 4
print("a after /= 4:", a) # Now a is 6.0
a %= 2 # Modulus of a by 2
print("a after %= 2:", a) # Now a is 0.0
a **= 3 # Raises a to the power of 3
print("a after **= 3:", a) # Now a is 0.0
a //= 1 # Floor division of a by 1
print("a after //= 1:", a) # Now a is 0.0

# Comparison Operators
print("\nComparison Operators")
a = 10
b = 20
print("a == b:", a == b) # Equal to
print("a != b:", a != b) # Not equal to 
print("a > b:", a > b) # Greater than
print("a < b:", a < b) # Less than
print("a >= b:", a >= b) # Greater than or equal to
print("a <= b:", a <= b) # Less than or equal to

# Logical Operators
print("\nLogical Operators")
# And Operator
print("AND Gate Truth Table")
print("False and False:", False and False) # Returns False
print("False and True:", False and True) # Returns False
print("True and False:", True and False) # Returns False
print("True and True:", True and True) # Returns True

# OR Operator
print("\nOR Gate Truth Table")
print("False or False:", False or False) # Returns False
print("False or True:", False or True) # Returns True
print("True or False:", True or False) # Returns True
print("True or True:", True or True) # Returns True

# NOT Operator
print("\nNOT Gate Truth Table")
print("not False:", not False) # Returns True
print("not True:", not True) # Returns False